
public class C_CaracteresEStrings {
	
	public static void main(String[] args) {

		char letra = 'a';						// char declara uma variavel que recebe apenas um caracter unicode. Colocado entre aspas simples
		
		System.out.println(letra);
		
		String frase = "Minha frase digitada";	// String declara um conjunto de caracteres. Colocado entre aspas duplas 
		
		System.out.println(frase);
		
		
	}

}
